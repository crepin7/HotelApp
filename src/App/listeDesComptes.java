    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String nomClient = nom.getText();
        String prenomClient = prenom.getText();
        String localisationClient = localisation.getText();
        String telClient = tel.getText();
        String motDePasseClient = motDePasse.getText();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/hotel", "root", "");
            
            insert = con1.prepareStatement("INSERT INTO client (nom, prenom, localisation, telephone, mot_de_passe) Values (?, ?, ?, ?, ?)");
            
            insert.setString(1, nomClient);
            insert.setString(2, prenomClient);
            insert.setString(3, localisationClient);
            insert.setString(4, telClient);
            insert.setString(5, motDePasseClient);
            
            insert.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Compte ajouté");
            
            table_update();
            
            nom.setText("");
            prenom.setText("");
            localisation.setText("");
            tel.setText("");
            motDePasse.setText("");
            
            nom.requestFocus();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(listeDesComptes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(listeDesComptes.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

